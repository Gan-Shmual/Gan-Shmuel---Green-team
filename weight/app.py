from flask import Flask, jsonify
import os

app = Flask(__name__)

@app.route("/health")
def health():
    return jsonify({
        "status": "ok",
        "service": os.getenv("SERVICE_NAME", "unknown")
    })

@app.route("/")
def index():
    return f"Hello from {os.getenv('SERVICE_NAME', 'unknown')} service!"

if __name__ == "__main__":
    port = int(os.getenv("PORT", 8000))
    app.run(host="0.0.0.0", port=port)
